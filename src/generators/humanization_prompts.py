def get_surgical_writing_rules() -> str:
    """获取「外科手术式写作」规则（补充现有人性化规则未覆盖的维度）"""
    return """
[叙事深度规则]

1. **禁止深度宣告（Show Don't Tell 强化）**：
   - 禁止叙述者直接宣告角色的领悟、成长或情感深度
   - ❌ "他终于明白了，真正的力量不在于修为，而在于心境"
   - ❌ "这一刻，她感受到了前所未有的孤独"
   - ❌ "他第一次意识到，自己一直以来都错了"
   - ✅ 用行为转变、沉默、一个不同于以往的选择来体现领悟
   - ✅ 用环境细节的主观感知变化来暗示情感（"风突然冷了"而非"他感到心寒"）
   - 原则：深度靠内容自证，读者自己感到"原来如此"，而非被告知

2. **旁白禁令（场景代替解释）**：
   - 禁止用旁白解释角色性格、动机、关系
   - ❌ "张三是个心思缜密的人，从不轻易相信别人"（旁白定性）
   - ✅ 让张三在具体场景中展现缜密：反复确认消息来源、留后手、对盟友的话也要验证
   - 禁止用旁白解释功法/势力背景超过2句，改用角色对话或行动中自然带出

3. **动词人格化**：
   - 不同角色使用不同的动词体系，动词本身传递性格
   - 老练角色："瞥"、"掂量"、"不动声色地"
   - 莽撞角色："冲"、"一把抓住"、"吼"
   - 阴沉角色："摩挲"、"低声"、"缓缓"
   - 同一个"看"的动作：老者"端详"、少年"瞪"、谋士"扫了一眼"
   - 每个动词都是一次性格判断，禁止所有角色共用同一套动词

4. **翻译腔净化**：
   - 以下句式是典型翻译腔，必须改写为自然中文：
   - ❌ "他的眼睛里充满了X" → ✅ 直接写反应（"他愣住了"、"瞳孔骤缩"）
   - ❌ "她发现自己正在X" → ✅ 去掉"发现自己"（"她不知不觉已经X"或直接写动作）
   - ❌ "这使得他不得不X" → ✅ "他只好X"、"没办法，只能X"
   - ❌ "作为一个X，他Y" → ✅ "他是X，Y"或直接省略身份从句
   - ❌ "在X的帮助下" → ✅ "靠着X"、"多亏X"
   - 检测方法：这句话翻成英文再翻回中文，还是原样？是→大概率翻译腔，重写
"""


