def get_rhythm_variation_rules() -> str:
    """获取节奏变化要求"""
    return """
[节奏变化要求]

1. **句子长度变化**：
   - 避免连续 3 个以上相同长度的句子
   - 短句（10 字以内）：用于强调、转折、情绪爆发
     示例："他愣住了。"、"不可能！"
   - 长句（30 字以上）：用于环境描写、心理活动、复杂情节
     示例："夕阳的余晖洒在青石板路上，将整个小镇染成了一片金黄，远处传来的钟声悠扬而绵长。"
   - 中等句子（15-25 字）：作为主体，承载主要叙述
     示例："林小凡深吸一口气，缓缓睁开了眼睛。"

2. **段落结构变化**：
   - 避免每段都是 3-5 句的固定模式
   - 适当使用单句段落（强调、转场）
     示例：
     "一切都结束了。"
   - 对话密集段落 vs 描写密集段落交替
   - 段落长度要有起伏（短段落 2-3 句，长段落 6-8 句）

3. **叙事节奏**：
   - 紧张场景：短句、快节奏、少描写、多动作
     示例："剑来！他一声暴喝。剑光闪过。血花飞溅。"
   - 平静场景：长句、慢节奏、多细节、重氛围
     示例："午后的阳光透过窗棂洒进来，茶香袅袅升起，他端起茶杯，轻轻吹了吹，慢慢品味着这难得的宁静时光。"
   - 避免全篇统一节奏（要有快慢交替）

4. **对话与叙述的节奏配合**：
   - 紧张对话：短促、打断、不完整句
     示例：
     "你——"
     "闭嘴！"
     "可是……"
     "我说了闭嘴！"
   - 平静对话：完整、从容、有停顿
     示例：
     "师父，我有个问题想请教。"他恭敬地说道。
     老者放下手中的茶杯，微微一笑："说吧，什么问题？"
"""


