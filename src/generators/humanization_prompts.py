def get_humanizer_zh_core_rules() -> str:
    """获取 Humanizer-zh 的 5 条核心原则（针对小说创作优化）"""
    return """
[Humanizer-zh 核心原则]

1. **删除填充短语**：
   - 去除开场白和强调性拐杖词
   - 避免："为了实现这一目标"、"由于……的事实"、"在这个时间点"、"值得注意的是"
   - 改用：直接陈述，简洁有力

2. **打破公式结构**：
   - 避免二元对比（"不仅……而且……"）
   - 避免戏剧性分段（每段都是转折）
   - 避免修辞性设置（"这不仅仅是……，而是……"）

3. **变化节奏**：
   - 混合句子长度（短句10字内，长句30字以上，中等句子为主）
   - 两项优于三项（避免三段式列举）
   - 段落结尾要多样化（不要每段都是总结句）

4. **信任读者**：
   - 直接陈述事实，跳过软化和辩解
   - 不要手把手引导（"让我们来看看……"）
   - 避免过度限定（"可能"、"也许"、"某种程度上"）

5. **删除金句**：
   - 如果听起来像可引用的语句，重写它
   - 避免刻意的哲理性总结
   - 用具体细节代替抽象概括
"""


