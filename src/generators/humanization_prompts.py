def replace_ai_words(text: str, replacement_dict: Dict[str, List[str]]) -> str:
    """替换AI化词汇为自然表达"""
    engine = HumanizationPromptEngine()
    result = text
    
    for ai_word, replacements in engine.ai_word_replacements.items():
        if ai_word in result:
            replacement = random.choice(replacements)
            result = result.replace(ai_word, replacement)
    
    return result

def inject_natural_expressions(text: str, density: float = 0.1) -> str:
    """注入自然化表达"""
    engine = HumanizationPromptEngine()
    sentences = text.split('。')
    
    for i, sentence in enumerate(sentences):
        if random.random() < density:
            # 随机选择一种自然化表达类型
            expr_type = random.choice(list(engine.natural_expressions.keys()))
            expr = random.choice(engine.natural_expressions[expr_type])
            
            # 在句子中适当位置插入表达
            if '，' in sentence:
                parts = sentence.split('，')
                insert_pos = random.randint(0, len(parts))
                parts.insert(insert_pos, expr)
                sentences[i] = '，'.join(parts)
    
    return '。'.join(sentences)

