from typing import Dict, Optional


def get_humanization_prompt(dialogue_ratio_target: float = 0.45) -> str:
    """获取统一的人性化写作提示词（合并了对话增强、简化策略）

    Args:
        dialogue_ratio_target: 目标对话比例，从 config.json 的 humanization.dialogue_ratio 读取
    """
    pct = f"{dialogue_ratio_target:.0%}"
    return f"""
[核心人性化要求]

1. **对话主导原则**（目标对话比例：{pct}）：
   - 用对话推进剧情，减少大段叙述
   - 人物说话要有口癖和习惯用语
   - 不同身份的人说话方式不同（老人用"老夫"、年轻人口语化）
   - 重要信息通过对话传达，人物关系通过对话展现

2. **叙述与对话差异化处理**：
   【对话部分】：
   - 适当加入停顿词："呃"、"那个"、"嗯"、"怎么说呢"
   - 允许语法不完整、重复、打断、插话、说错话
   - 体现人物个性和情感状态
   - 不确定词："大概"、"可能"、"也许"、"估计"

   【叙述部分】：
   - 保持流畅通顺，严禁加入任何停顿词
   - 避免重复和啰嗦，用词精准，节奏明快
   - 重点突出动作、环境、心理的生动描写
   - 句式完整但富于变化，长短句搭配

3. **词汇自然化策略**：
   - 禁止使用：伴随着、与此同时、紧接着、毫无疑问、显而易见、不言而喻、众所周知
   - 优先使用：然后、接着、突然、结果、没想到、谁知道、竟然、居然
   - 专业术语用大白话解释

4. **句式协调化**：
   - 绝对禁止连续3句使用相同句式结构
   - 混合使用：长句、短句、不完整句、感叹句、疑问句
   - 叙述部分注重简洁有力，避免冗余

5. **情感真实化**：
   - 对话中人物会犹豫、矛盾、说错话
   - 叙述中情绪变化要有过程，不要瞬间转换
   - 加入尴尬、无奈、吐槽等真实情感
   - 用身体语言和环境细节表达内心状态
   - 加入无关紧要的小动作：挠头、叹气、翻白眼、摆手

6. **配角故事线**：
   - 并非所有场景都有主角在场
   - 围绕配角展开的故事最终回归主角生活
   - 场景间衔接流畅，通过行动、对话、描写过渡

[严禁在叙述部分使用的词汇清单]
以下词汇严禁在叙述部分使用，仅限对话中使用：
- "呃"、"那个"、"嗯"、"怎么说呢"、"你知道的"
- "哎呀"、"哟"、"嘿"
- 其他表示犹豫、停顿的口语化词汇

[写作示例对比]
AI化写作：
"伴随着修炼功法的运转，林小凡感受到体内元气正在按照既定轨迹流淌。"

人性化改写：
林小凡闭着眼睛默念口诀，感觉肚子里有股暖流在转圈。
"咦……这感觉还挺舒服的。"他心里想着，嘴角不自觉地翘了起来。
"""


